#pragma once

class Sprite;
class InputSystem;
class MapCreate;
class HeartUI;
class Goal;
class Player;

class SceneBase
{
public:
    SceneBase() {};

    virtual ~SceneBase() {};

    virtual SceneBase* update() = 0;

    static int  GetScene() { return mIsScene; };
    virtual void SetScene(int _isScene) { mIsScene = _isScene; }; //シーンをセット

    enum Scene
    {
        // その他
        other,
        // タイトル
        title,
        // チュートリアル
        tutorial,
        // ステージセレクト画面
        stageSelect,
        // ステージ1
        stage01,
        // ステージ2
        stage02,
        // ゲームクリア
        gameClear,
        // ゲームオーバー
        gameOver,
        // コンティニュー 
        Continue
    };

protected:
    Sprite*      mSprite;
    //入力管理クラス
    InputSystem* mInputSystem;

    MapCreate*   mMapCreate;
    Player*      mPlayer;
    Goal*        mGoalLine;
    HeartUI*     mHeartUI;

    bool         mClearFlag;

    int          mNextSceneCount;

private:
    static int   mIsScene;
};